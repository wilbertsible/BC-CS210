public class Difference { //Create the class
	
	public static void main(String[] args) { // Create the method
		
		System.out.println("What is the difference between"); // Printing
		System.out.println("a ' and a \"?  Or between a \" and a \\\"?"); //Use escape sequence for " and \
		System.out.println();//Puts a line in between the statements
		System.out.println("One is what we see when we're typing our program.");
		System.out.println("The other is what appears on the \"console.\""); //Use escape sequence for "
	}
}